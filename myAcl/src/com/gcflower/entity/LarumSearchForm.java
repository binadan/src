package com.gcflower.entity;

public class LarumSearchForm implements java.io.Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String startTime;//开始时间
	private String endTime;//结束时间
	private String houseId;//房屋编号
	private String hostId;//主机ID
	private String LState;//状态
	
	/** default constructor */
	public LarumSearchForm() {
	}
	public LarumSearchForm(String startTime, String endTime, String houseId,
			String hostId, String lState) {
		super();
		this.startTime = startTime;
		this.endTime = endTime;
		this.houseId = houseId;
		this.hostId = hostId;
		LState = lState;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getHouseId() {
		return houseId;
	}
	public void setHouseId(String houseId) {
		this.houseId = houseId;
	}
	public String getHostId() {
		return hostId;
	}
	public void setHostId(String hostId) {
		this.hostId = hostId;
	}
	public String getLState() {
		return LState;
	}
	public void setLState(String lState) {
		LState = lState;
	}

}
